  <li>Données de salariés (paie) : identité, salaire, numéros IPRES/CSS</li>
  <li>Données de prospects et contacts commerciaux</li>
</ul>
<h3>Comptes du portail client</h3>
<ul>
  <li>Identité et coordonnées du contact client</li>
  <li>Pièces et documents transmis par le client</li>
</ul>

