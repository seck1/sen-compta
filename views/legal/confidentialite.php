<p>Vos données sont traitées pour les finalités suivantes :</p>
<ul>
  <li>Fournir le service de gestion comptable (tenue de comptabilité, états financiers, paie, fiscalité)</li>
  <li>Gérer les comptes utilisateurs et l'authentification sécurisée</li>
  <li>Permettre la communication entre le cabinet et ses clients (portail client)</li>
  <li>Assurer la sécurité, la prévention de la fraude et le respect des obligations légales</li>
  <li>Améliorer le service et le support utilisateur</li>
</ul>

