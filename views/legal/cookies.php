<h2>1. Qu'est-ce qu'un cookie ?</h2>
<p>Un cookie est un petit fichier déposé sur votre appareil lors de la visite d'un site. Il permet notamment de maintenir votre session et de mémoriser vos préférences.</p>

<h2>2. Cookies utilisés par SenCompta</h2>
<p>SenCompta utilise un nombre minimal de cookies, strictement nécessaires au fonctionnement du service :</p>
<h3>Cookies strictement nécessaires (toujours actifs)</h3>
<ul>
  <li><strong>Cookie de session</strong> (<code>PHPSESSID</code>) : maintient votre connexion sécurisée pendant la navigation. Sans lui, vous ne pourriez pas rester connecté.</li>
  <li><strong>Cookie de sécurité (CSRF)</strong> : protège contre les attaques de falsification de requête.</li>
  <li><strong>Préférence de consentement</strong> : mémorise votre choix concernant les cookies.</li>
</ul>
<p>Ces cookies ne nécessitent pas de consentement car ils sont indispensables au service.</p>

<h3>Cookies de mesure / tiers</h3>
<p>SenCompta n'utilise <strong>pas</strong> de cookies publicitaires ni de traceurs tiers à des fins marketing. <span class="lg-fill">[À ADAPTER si vous ajoutez un outil de statistiques type Matomo/Google Analytics]</span></p>

<h2>3. Gestion des cookies</h2>
<p>Vous pouvez configurer votre navigateur pour refuser ou supprimer les cookies. Toutefois, le refus des cookies strictement nécessaires empêchera le bon fonctionnement de l'application (impossibilité de se connecter).</p>

